void main() {
  // Strings
  String firstName = "Kennel";
  print("String: $firstName");
  // Integer
  int age = 42;
  print("Integer: $age");
  // Doubles
  double weight = 83.2;
  print("Double: $weight");
  // Booleans
  bool myBool = false;
  print("Boolean: $myBool");
  // Dynamics
  dynamic fullName = "Kennel KASSI";
  print("Dynamic: $fullName");
}
