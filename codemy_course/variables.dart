void main() {
  // strings
  var name = 'John';
  print(name);
  String nom = "John";
  print(nom);
  // integer
  int x = 42;
  print(x);
  // dynamic
  dynamic firstName = "Tim";
  print(firstName);
  dynamic age = 12;
  print(age);
  // const & final
  const String fullName = "Kennel KASSI";
  print(fullName);
  final String nickName = "Kenny";
  print(nickName);
}
